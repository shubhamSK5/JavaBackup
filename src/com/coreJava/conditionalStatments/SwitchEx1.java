package com.coreJava.conditionalStatments;

public class SwitchEx1 {
	public static void main(String[] args) {
		char a='Z';
		
		switch(a) {
		case 'a':System.out.println("a ia a vowel");
		break;
		case 'A':System.out.println("A ia a vowel");
		break;
		case 'e':System.out.println("e ia a vowel");
		break;
		case 'E':System.out.println("E ia a vowel");
		break;
		case 'i':System.out.println("i ia a vowel");
		break;
		case 'I':System.out.println("I ia a vowel");
		break;
		case 'o':System.out.println("o ia a vowel");
		break;
		case 'O':System.out.println("O ia a vowel");
		break;
		case 'u':System.out.println("u ia a vowel");
		break;
		case 'U':System.out.println("U ia a vowel");
		break;
		
		default:System.out.println("NOT a vowel");
		}
	}

}
