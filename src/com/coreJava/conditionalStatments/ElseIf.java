package com.coreJava.conditionalStatments;

public class ElseIf {

	public static void main(String[] args) {
		int time=14;
		
		if (time<12)
		{
			System.out.println("Good Morning");
		}
		else if(time<16)
		{
			System.out.println("Good Afternoon");
		}
		else
		{
			System.out.println("Good Night");
		}

	}

}
